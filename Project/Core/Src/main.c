  ring_buffer_init(&ring_buffer_keypad, keypad_buffer, 10);


  // OLED Display Initialization
  ssd1306_Init();
  ssd1306_Fill(Black);
  ssd1306_SetCursor(20, 20);
  ssd1306_WriteString("Welcome", Font_11x18, White);
  ssd1306_UpdateScreen();
  HAL_Delay(500);

  keypad_init(); // Initialize the keypad functionality

 // Initialize actions for ecg module.
  typedef enum {
       ACCION_1,
       ACCION_2,
       ACCION_3
   } Estado;

   uint32_t start_time = 0;
   Estado actual_state = ACCION_1;
   start_time = HAL_GetTick();

